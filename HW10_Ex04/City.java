package bg.swift.HW10_Ex04;

public class City {
	private int distance;
	private int provision;
	
	public City(int distance, int provision) {
		this.distance = distance;
		this.provision = provision;
	}

	public int getDistance() {
		return distance;
	}

	public int getProvision() {
		return provision;
	}
	
}
